    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(rootPane, "THERE ARE NO DATA IN THE TABLE");
        } else {
            try {
                DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
                for (int i = 0; i < df.getRowCount(); i++) {
                    String s0 = df.getValueAt(i, 0).toString();
                    String s1 = df.getValueAt(i, 1).toString();
                    String s2 = df.getValueAt(i, 2).toString();
                    String s3 = df.getValueAt(i, 3).toString();
                    String s4 = df.getValueAt(i, 4).toString();
                    String insert = "INSERT INTO stock(product_id,product,qty,unit_price,selling_price)VALUES('" + s0 + "','" + s1 + "','" + s2 + "','" + s3 + "','" + s4 + "')";
                    Database.DB.IUD(insert);
                }
                df.setRowCount(0);
                JOptionPane.showMessageDialog(rootPane, "DONE");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "ALREADY EXISTS");
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

