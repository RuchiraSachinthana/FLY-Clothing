    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        int rowcount = jTable1.getRowCount();
        if (rowcount == 0) {
            try {
                String load = "SELECT * FROM stock";
                ResultSet rs = Database.DB.search(load);
                DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
                while (rs.next()) {
                    Vector v = new Vector();
                    v.add(rs.getString("product_id"));
                    v.add(rs.getString("product"));
                    v.add(rs.getString("qty"));
                    v.add(rs.getString("unit_price"));
                    v.add(rs.getString("selling_price"));
                    df.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        } else {
        }

    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTable1.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "FIRST CHOOSE WHAT TO REMOVE");
        } else {
            try {
                DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
                String s = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
                int aa = jTable1.getSelectedRow();
                String delete = "DELETE FROM stock WHERE product_id = '" + s + "'";
                Database.DB.IUD(delete);
                df.removeRow(aa);
                JOptionPane.showMessageDialog(rootPane, "DELETED");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (this.jTable1.getSelectionModel().isSelectionEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "FIRST SELECT THE ONE YOU WANT TO UPDATE");
        } else {
            try {
                int i = jTable1.getSelectedRow();
                TableModel model = jTable1.getModel();
                this.dispose();
                new Update().setVisible(true);
                Update.productid.setText(model.getValueAt(i, 0).toString());
                Update.product.addItem(model.getValueAt(i, 1).toString());
                Update.qty.setText(model.getValueAt(i, 2).toString());
                Update.unitprice.setText(model.getValueAt(i, 3).toString());
                Update.sellingprice.setText(model.getValueAt(i, 4).toString());
            } catch (Exception e) {
                this.dispose();
                JOptionPane.showMessageDialog(rootPane, "FIRST SELECT THE ONE YOU WANT TO UPDATE");
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

